  private static void loadValues() {
    for(Translation value : values) {
      BY_ID.put(value.id(), value);
      BY_NAME.put(value.name().toLowerCase(), value);
    }
  }

  private final String id, name;

  private Translation(String id) {
    this.id = id;
    Matcher match = pat.matcher(id);
    if(match.find()) {
      Matcher u = up.matcher(match.group(1));
      StringBuilder prefix = new StringBuilder();
      String group = match.group(1);
      boolean found = u.find();
      char[] chars = group.toCharArray();
      for(int i = 0; i < chars.length; i++) {
        if(found) {
          if(i == u.start()) {
            prefix.append(u.group(1) != null ? u.group(1).toUpperCase() + "_" : "_").append(u.group(2).toUpperCase());
            i = u.end() - 1;
            found = u.find();
            continue;
          }
        }
        prefix.append(chars[i]);
      }
      name = prefix.toString().replace(".", "_").toUpperCase();
    } else name = id.replace(".", "_").toUpperCase();
    BY_ID.put(id, this);
    BY_NAME.put(name, this);
    values.add(this);
  }

  public String id() {
    return this.id;
  }

  public String name() {
    return name;
  }

  public String toString() {
    return id();
  }

  public static Translation byId(String id) {
    if(BY_ID.isEmpty()) loadValues();
    return BY_ID.getOrDefault(id, BY_NAME.get(id));
  }

  public static Translation byName(String id) {
    if(BY_NAME.isEmpty()) loadValues();
    return BY_NAME.getOrDefault(id, BY_ID.get(id));
  }
